function TBL(rows, widths){
  return new Table({ width:{ size:100, type:WidthType.PERCENTAGE },
    rows: rows.map((r,ri)=> new TableRow({ tableHeader: ri===0, children: r.map((cell,ci)=> new TableCell({
      borders:cB, verticalAlign:VerticalAlign.CENTER, margins:{top:30,bottom:30,left:70,right:70},
      shading: ri===0 ? {fill:BAND,type:ShadingType.CLEAR,color:"auto"} : (ri%2===0?{fill:"F7F9FC",type:ShadingType.CLEAR,color:"auto"}:undefined),
      width: widths ? { size:widths[ci], type:WidthType.PERCENTAGE } : undefined,
      children:[ new Paragraph({ alignment: ri===0 ? AlignmentType.CENTER : (ci===0?AlignmentType.LEFT:AlignmentType.CENTER),
        children:[ new TextRun({ text:String(cell), bold: ri===0, size:16, color: ri===0?NAVY:"000000" }) ] }) ]
    })) })) });
}
